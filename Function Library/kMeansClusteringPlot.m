function kMeansClusteringPlot(vectors, classificationIdentification, kCenters, classificationNum)
    hold off;
    colorset = {[0 0.4470 0.7410], [0.8500 0.3250 0.0980],...
        [0.9290 0.6940 0.1250], [0.4940 0.1840 0.5560],...
        [0.4660 0.6740 0.1880], [0.3010 0.7450 0.9330],...
        [0.6350 0.0780 0.1840]};
    for i = 1:classificationNum
        col = colorset{i};
        cls0 = vectors(classificationIdentification == i, :);
        scatter3(cls0(:, 1), cls0(:, 2), cls0(:, 3), 1, 'filled',...
            'MarkerFaceColor', col,...
            'MarkerFaceAlpha', 0.2);
        hold on;
    	scatter3(kCenters(i, 1), kCenters(i, 2), kCenters(i, 3), 72, 's',...
            'MarkerFaceColor', col,...
            'MarkerEdgeColor', 'k');
    end
    xlabel('Red');
    ylabel('Green');
    zlabel('Blue');
end